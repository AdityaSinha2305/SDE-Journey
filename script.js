// Modal triggers configurations
function initModalActions() {
  const modal = document.getElementById("reset-confirm-modal");
  const openBtn = document.getElementById("open-reset-modal-btn");
  const closeBtn = document.getElementById("close-reset-modal-btn");
  const confirmBtn = document.getElementById("confirm-reset-btn");

  if (openBtn && modal) {
    openBtn.addEventListener("click", (e) => {
      e.preventDefault();
      modal.classList.remove("hidden");
    });
  }

  if (closeBtn && modal) {
    closeBtn.addEventListener("click", (e) => {
      e.preventDefault();
      modal.classList.add("hidden");
    });
  }

  if (confirmBtn && modal) {
    confirmBtn.addEventListener("click", (e) => {
      e.preventDefault();

      // Synchronously clearing local state variables
      state.taskStatuses = {};
      state.taskLinks = {};
      state.taskNotes = {};
      state.focusMinutes = 0;
      state.streak = 0;
      state.lastActiveDate = null;
      state.currentMonth = 0;
      state.activeTrack = "all";

      // Securely writing cleared keys to localStorage
      saveState();

      // Hide confirmation modal
      modal.classList.add("hidden");

      // Wipe DOM values explicitly
      const focusTimeDisplay = document.getElementById(
        "accumulated-focus-time",
      );
      const loggerMinutesDisplay = document.getElementById(
        "logger-minutes-display",
      );
      if (focusTimeDisplay) focusTimeDisplay.textContent = "0";
      if (loggerMinutesDisplay) loggerMinutesDisplay.textContent = "0 Mins";

      const streakCountDisplay = document.getElementById("streak-count");
      if (streakCountDisplay) streakCountDisplay.textContent = "0";

      // Re-calculate math and fully reload interactive checklist elements
      renderAll();
    });
  }
}

