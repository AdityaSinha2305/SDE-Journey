// Cycle topic state between todo -> progress -> mastered
function cycleTaskStatus(taskId) {
  const current = state.taskStatuses[taskId] || "todo";
  let next = "todo";
  if (current === "todo") {
    next = "progress";
  } else if (current === "progress") {
    next = "mastered";
  } else {
    next = "todo";
  }
  state.taskStatuses[taskId] = next;
  saveState();
  logDailyActivity();
  renderAll();
}

