// Metric algorithms
function calculateMetrics() {
  let totalTasks = 0;
  let masteredCount = 0;
  let inProgressCount = 0;

  let totalFe = 0,
    masteredFe = 0,
    progressFe = 0;
  let totalBe = 0,
    masteredBe = 0,
    progressBe = 0;
  let totalDsa = 0,
    masteredDsa = 0,
    progressDsa = 0;

  const monthDataTracker = SYLLABUS.map(() => ({
    total: 0,
    weightedProgress: 0,
  }));

  SYLLABUS.forEach((month) => {
    const monthIndex = month.monthIndex;
    month.weeks.forEach((week) => {
      week.tasks.forEach((task) => {
        totalTasks++;
        monthDataTracker[monthIndex].total++;

        const status = state.taskStatuses[task.id] || "todo";
        let progressWeight = 0; // todo = 0

        if (status === "mastered") {
          masteredCount++;
          progressWeight = 1;

          if (task.track === "frontend") masteredFe++;
          else if (task.track === "backend") masteredBe++;
          else if (task.track === "dsa") masteredDsa++;
        } else if (status === "progress") {
          inProgressCount++;
          progressWeight = 0.5;

          if (task.track === "frontend") progressFe++;
          else if (task.track === "backend") progressBe++;
          else if (task.track === "dsa") progressDsa++;
        }

        monthDataTracker[monthIndex].weightedProgress += progressWeight;

        if (task.track === "frontend") totalFe++;
        else if (task.track === "backend") totalBe++;
        else if (task.track === "dsa") totalDsa++;
      });
    });
  });

  // Math outputs
  const totalWeighted = masteredCount + inProgressCount * 0.5;
  const globalPct =
    totalTasks > 0 ? Math.round((totalWeighted / totalTasks) * 100) : 0;

  const globalPctNode = document.getElementById("global-progress-pct");
  if (globalPctNode) globalPctNode.textContent = `${globalPct}%`;

  const masteredCountNode = document.getElementById("stats-mastered-count");
  if (masteredCountNode) masteredCountNode.textContent = masteredCount;

  const progressCountNode = document.getElementById("stats-progress-count");
  if (progressCountNode) progressCountNode.textContent = inProgressCount;

  const circleNode = document.getElementById("global-progress-circle");
  if (circleNode) {
    const circleOffset = 251 - (251 * globalPct) / 100;
    circleNode.style.strokeDashoffset = circleOffset;
  }

  // Compute Category progression widths
  const fePct =
    totalFe > 0
      ? Math.round(((masteredFe + progressFe * 0.5) / totalFe) * 100)
      : 0;
  const feBarNode = document.getElementById("frontend-progress-bar");
  if (feBarNode) feBarNode.style.width = `${fePct}%`;

  const bePct =
    totalBe > 0
      ? Math.round(((masteredBe + progressBe * 0.5) / totalBe) * 100)
      : 0;
  const beBarNode = document.getElementById("backend-progress-bar");
  if (beBarNode) beBarNode.style.width = `${bePct}%`;

  const dsaPct =
    totalDsa > 0
      ? Math.round(((masteredDsa + progressDsa * 0.5) / totalDsa) * 100)
      : 0;
  const dsaBarNode = document.getElementById("dsa-progress-bar");
  if (dsaBarNode) dsaBarNode.style.width = `${dsaPct}%`;

  // Sidebar navigation targets
  monthDataTracker.forEach((data, index) => {
    const monthPct =
      data.total > 0
        ? Math.round((data.weightedProgress / data.total) * 100)
        : 0;
    const element = document.getElementById(`month-pct-${index}`);
    if (element) {
      element.textContent = `${monthPct}%`;
      if (monthPct === 100) {
        element.className =
          "text-xs font-semibold px-2.5 py-1 rounded bg-emerald-500/20 text-emerald-400 border border-emerald-500/10";
      } else if (monthPct > 0) {
        element.className =
          "text-xs font-semibold px-2.5 py-1 rounded bg-amber-500/20 text-amber-400 border border-amber-500/10";
      } else {
        element.className =
          "text-xs font-semibold px-2.5 py-1 rounded bg-zinc-950/40 text-zinc-500";
      }
    }
  });
}

